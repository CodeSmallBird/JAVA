package cn.tedu.object;

// Eclipse本身是一个实时编译
public class EclipseKeysDemo {

	// Alt + / 快捷提示键
	public static void main(String[] args) {
		// ctrl+/ 单行注释/取消单行注释
		// ctrl + shift + / 多行注释
		// ctrl + shift + \ 取消多行注释
		// Ctrl + D 删除整行
		// Alt + ↑ 整行上移
		// Alt + ↓ 整行下移
		// Ctrl + Alt + ↑ 复制整行，把复制的放在原来的代码上面
		// Ctrl + Alt + ↓ 复制整行，把复制的放在原来的代码下 面
		// Alt + Shift + S， 选择Format， 调整格式
		System.out.println("hello World");

	}

}
