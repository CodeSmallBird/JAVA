package cn.tedu.yhMis.pojo;

public class Door {
	
	private Integer id;
	private String doorName;
	private String doorTel;
	private String doorAddr;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDoorName() {
		return doorName;
	}
	public void setDoorName(String doorName) {
		this.doorName = doorName;
	}
	public String getDoorTel() {
		return doorTel;
	}
	public void setDoorTel(String doorTel) {
		this.doorTel = doorTel;
	}
	public String getDoorAddr() {
		return doorAddr;
	}
	public void setDoorAddr(String doorAddr) {
		this.doorAddr = doorAddr;
	}
}
