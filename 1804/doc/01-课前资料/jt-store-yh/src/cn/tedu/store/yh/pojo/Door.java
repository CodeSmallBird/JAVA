package cn.tedu.store.yh.pojo;

public class Door {
	private Integer id;	//属性
	private String name;
	private String tel;
	//快捷键 shift+alt+s
	//方法get获取/set设置
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
